#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int	x;
	int	y;

	x = 0;
	y = 0;
	while (src[y])
		dst[x++] = src[y++];
	dst[x] = '\0';
	x = 0;
	return (dst);
}
