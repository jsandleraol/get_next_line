#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int	x;
	int	y;

	x = 0;
	y = -1;
	while (s1[x])
		x++;
	while (s2[++y])
		s1[x + y] = s2[y];
	s1[x + y] = '\0';
	return (s1);
}
